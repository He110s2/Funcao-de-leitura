def soma_quatro_subtrai_dez():
    num1 = int(input("Digite o 1ยบ valor inteiro: "))
    num2 = int(input("Digite o 2ยบ valor inteiro: "))
    num3 = int(input("Digite o 3ยบ valor inteiro: "))
    num4 = int(input("Digite o 4ยบ valor inteiro: "))
    valor_interno = 10
    resultado = (num1 + num2 + num3 + num4) - valor_interno
    print("Resultado da soma menos 10:", resultado)